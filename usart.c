#include <reg52.h>
#include "ucharuint.h"
	

/******************************************************************
函 数: void Uart_Init(void)
功 能: 串口初始化，波特率为9600(这个不会，上网百度)
参 数: 无
返回值: 无
*******************************************************************/
void Uart_Init(void)
{  


TMOD=0x20;
TH1=0xfD;
TL1=0xfD;
TR1=1;
//相当于SCON=0x50
REN=1;
SM0=0;
SM1=1;
	
EA=1;
ES=1;


}